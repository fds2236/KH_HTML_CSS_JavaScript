let isAdult = false;
let age = prompt("나이를 입력하세요 : ", "");
age = Number(age); // Number : 문자타입을 숫자타입으로 바꿔주는 형변환
isAdult = (age > 18) ? true : false;
if (isAdult) document.write("<h3>당신은 성인입니다.</h3>");
else document.write("<h3>당신은 성인이 아닙니다.</h3>");
