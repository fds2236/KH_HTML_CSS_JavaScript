let test3 = 100;
let test4 = 200;
console.log("합 : " + (test3 + test4)); // 출력 :  300

let test5 = "100";
let test6 = 200;
console.log(`합 : ${test5 + test6}`); // 출력 :  100200(문자열로 그대로 붙어버림)

let dan = 3;
let gugu = 8;
let result = Math.round(dan * gugu);
console.log(result); // 출력 : 24
console.log(`${dan} 곱하기 ${gugu}은 ${dan * gugu}입니다.`); // 출력 :  3 곱하기 8은 24입니다.
